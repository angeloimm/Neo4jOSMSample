package it.olegan.routing.exception;

public class GraphCreatorException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7367150234034132775L;

	public GraphCreatorException() {

	}

	public GraphCreatorException(String message) {
		super(message);

	}

	public GraphCreatorException(Throwable cause) {
		super(cause);

	}

	public GraphCreatorException(String message, Throwable cause) {
		super(message, cause);

	}

	public GraphCreatorException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
